$("#viewOpen").click(function(){
    if($("#viewLinks").hasClass("visible")){
     $("#viewLinks").addClass("hidden");  
     $("#viewLinks").removeClass("visible");  
    }
    else{
     $("#viewLinks").addClass("visible");  
     $("#viewLinks").removeClass("hidden");  
    }
});
